				{
					console.log(pare_data.msg);
					jQuery('#page-process-msg').addClass("error_message");
					jQuery(".ajax-loader").hide();
					jQuery('#page-process-msg').show();
					jQuery('#page-process-msg').html(pare_data.msg);
				}
			});
