function checkitinerary_form()
{
	jQuery(".ajax-loader").show();
	jQuery('#page-process-msg').hide();
	jQuery('#page-process-msg').removeClass("text-success error_message");
    if(jQuery('#selected_itinerary').val()!='')
    {
        var process = 0;
        var albm_type ='';
        var albm_name ='';
        if(jQuery('#selected_itinerary').val()=='create_new')
        {
            if(jQuery('#new_itinerary_text').val()!='')
            {
                process = 1;
                albm_type = 'new';
				jQuery(".ajax-loader").hide();
                albm_name = jQuery('#new_itinerary_text').val();
                
            }else
            {
                process =0;
                jQuery('#new_itinerary_text').focus();
				jQuery('#page-process-msg').addClass("error_message");
				jQuery(".ajax-loader").hide();
				jQuery('#page-process-msg').show();
                jQuery('#page-process-msg').html('Please enter itinerary name');
                return false;
            }
        }else
        {
            albm_name = jQuery('#selected_itinerary').val();
            albm_type = 'old';
            process = 1;
        }
        
	   if(process==1)
	   {
			var attr_id = jQuery('#attr_id_in').val();
			$.post( site_url+"/ajax/ajax_call.php?mode=add_attraction_itinerary", {attr_id:attr_id,album_type:albm_type,albm:albm_name}, function( data ) {
				var pare_data = JSON.parse(data);
