<p style="text-align: justify;">
 <b>Гид по нью-йоркской кухне</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Хотите ли вы просто перекусить или ищете ресторан для торжественного ужина – в Олбани много вариантов на выбор. «Местные фавориты – это спорт-бар “Juniors”, буррито-бар “Bombers” и новый бар, где продают вкуснейшие бургеры “Crave”», - рекомендует <b>Тара Хеффернан</b> из колледжа <b>Saint Rose</b>. Cамое популярное место для субботнего или воскресного бранча -&nbsp;“Caf&#233; Madison”, но&nbsp;приготовьтесь отстоять нешуточную очередь.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В Ньюарке, как утверждает <b>Джерри Докармо </b>из<b> Harvest Institute</b>, - «сотни ресторанов с разнообразнейшей гастрономией», и наиболее популярные, по его мнению, “Brasilia Grill” для тех, кто хочет попробовать барбекю, и “Hobby’s Deli” для любителей сэндвичей с газировкой.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Принимая более 50 миллионов туристов в год, Нью-Йорк, конечно, может предложить множество мест для перекуса между посещениями достопримечательностей. Здесь есть и высококлассные рестораны со звёздными шеф-поварами, и специализированные заведения для гурманов, и обычные палатки на колесах, расположившиеся вдоль тротуаров. «Возможности пробовать что-либо новое в Нью-Йорке бесконечны, - говорит <b>Барбара Дик </b>из<b> </b><b>New </b><b>York </b><b>Language </b><b>Centre</b>. - В “Astoria” подают греческую кухню, в “Jackson Heights” – индийскую, в бруклинском “Brighton Beach” – русскую, а в “Flushing” – китайскую».
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 «Хот дог, бейгл и пицца стали уже символами Нью-Йорка», - считает <b>Джош Анкорс </b>из<b> </b><b>L&#233;</b><b>man </b><b>Manhattan </b><b>Preparatory </b><b>School</b>. А хот доги в “Nathan’s Hot Dog” на Кони Айленд настоятельно рекомендует <b>Лавия Яламанчи </b>из<b> </b><b>Rennert </b><b>International</b>. «За пиццей лучше отправляйтесь в “Paulie Gee’s” или “Roberta’s” в Бруклине или в “Grimaldi’s” на Манхэттене», - говорит <b>Лайна МакРай </b>из<b> </b><b>Study </b><b>Group</b>.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Она также рекомендует “Momofuku” на Манхэттене, где можно попробовать недорогую китайскую еду. “Shake Shack” предлагает&nbsp;классические американские бургеры и коктейли, а “Katz’s Deli” в Нижнем Ист-Сайде обязателен для любителей традиционного нью-йоркского пастрами-сэндвича.
</p>
<p style="text-align: justify;">
