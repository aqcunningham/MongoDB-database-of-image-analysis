                                    <h1>Нью-Йорк</h1>
<p class="" style="text-align: justify;">
 <i>Обилие достопримечательностей и развлечений, которые может предложить штат Нью-Йорк, привлекает не только множество туристов, но и студентов.</i>
</p>
<p class="" style="text-align: center;">
 <img width="640" alt="5443157673_9854560f3b_z.jpg" src="/upload/medialibrary/05f/05ff9bef5fde16d5a5c36182b6d578dc.jpg" height="308" title="5443157673_9854560f3b_z.jpg"><br>
</p>
<p class="" style="text-align: center;">
 <span style="color: #666666; background-color: #ffffff;">Photo credit: </span><a href="https://www.flickr.com/photos/danielmennerich/5443157673/" style="color: #0abfbc;">Daniel Mennerich</a><span style="color: #666666; background-color: #ffffff;"> via </span><a href="https://visualhunt.com/" style="color: #fc354c;">Visual hunt</a><span style="color: #666666; background-color: #ffffff;"> / </span><a href="https://creativecommons.org/licenses/by-nc-nd/2.0/" style="color: #fc354c;">CC BY-NC-ND</a><br>
</p>
<p style="text-align: center;" class="">
 <b><i><br>
 </i></b>
</p>
<p style="text-align: center;" class="">
 <b><i>Интересный факт: в Нью-Йорке проживает&nbsp;больше людей, чем в 39 других штатах (из 50).</i></b>
</p>
<p style="text-align: center;" class="">
 <b><i><br>
 </i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Как самый большой город Америки Нью-Йорк вобрал в себя всё разнообразие культур и традиций. <b>Лавия Яламанчи</b> из школы <b>Rennert </b><b>International</b> считает, что такое многонациональное окружение даёт дополнительные преимущества в обучении: «Вы слышите английский язык во множестве акцентных вариантов, тренируя тем самым свой лингвистический слух». «Каждый студент может найти в Нью-Йорке что-нибудь родное – будь то еда или язык – что поможет пережить культурный шок и быстро адаптироваться к жизни в чужой стране», - добавляет <b>Джош Анкорс</b> из средней школы <b>L&#233;man Manhattan Preparatory School.</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Смешение культур и национальностей не сводится только к разнообразию в самом мегаполисе. <b>Джерри Докармо </b>из<b> Harvest Institute</b> восхищается многообразием Ньюарка, который в своё время был колонизирован сначала итальянскими фермерами, а потом португальцами.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Колледж <b>Saint Rose</b> находится в Олбани, столице штата Нью-Йорк. <b>Тара Хеффернан</b> отмечает, что Олбани – сердце центрального региона, и рекомендует прогулку по исторической улице Ларк, на которой располагается множество ресторанов с различными национальными кухнями.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В самом Нью-Йорке школа <b>New York Language Centre</b>, например, организует дополнительные бесплатные мероприятия, которые позволяют студентам активно общаться во время посещения достопримечательностей, бейсбольных матчей и т.п. По словам <b>Лайны МакРай </b>из<b> study Group</b>, помимо очевидных символов города – Эмпайр-Стейт-Билдинг, Центральный парк, Пятая Авеню, Бродвей и Таймс-Сквер, – в Нью-Йорке находится более 1700 парков, спортивных площадок и прочих мест для отдыха.
</p>
<p style="text-align: center;" class="">
 <img width="600" alt="statue-of-liberty-new-york-city-cityscape.jpg" src="/upload/medialibrary/6c8/6c8586bccb284c7c4e8667ddcd0864af.jpg" height="375" title="statue-of-liberty-new-york-city-cityscape.jpg"><br>
</p>
<p style="text-align: center;" class="">
 <span style="color: #666666; background-color: #ffffff;">Photo via </span><a href="https://visualhunt.com/photos/city/" style="color: #fc354c;">Visual hunt</a><br>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Хотя город состоит из 5 районов, большинство туристов прежде всего представляют себе Манхэттен, когда речь заходит о Нью-Йорке (другие городские районы: Бруклин, Квинс, Стейтен Айленд, Бронкс). Лайна утверждает, что Квинс – наиболее этнически разнообразный район в&nbsp;США: «Вы можете каждый день выбирать новый квартал для исследования». <b>Дэвид Хьюс</b> из школы <b>ILSC New York</b> добавляет: «Город полностью безопасен даже в таких районах, как Гарлем, о которых до сих пор сохраняется дурная слава».
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>Транспорт</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Несмотря на обширную территорию Нью-Йорка (140000 км<sup>2</sup>) его легко исследовать на общественном транспорте: транспортная система города работает круглосуточно. При этом, например, в школах Summer Study Programs и колледже Saint Rose выдаются бесплатные транспортные карты; в Ньюарке на проезд студентам предоставляются скидки.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 &nbsp;
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>5 мест, которые нужно посетить в Нью-Йорке</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b><i>1.&nbsp;</i></b><b><i>Прогуляться по Хай Лайн</i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 «Пройтись по наземному парку в Западном Манхэттене – это не только прекрасное времяпрепровождение в хорошую погоду», - говорит <b>Барбара Дик </b>из<b> </b><b>New </b><b>York </b><b>Language </b><b>Centre</b>, - «но и занимательный урок истории: когда-то здесь проходила железная дорога, которая активно использовалась для доставки товаров в город. Сейчас это одно из наиболее популярных мест среди молодёжи».
</p>
<p style="text-align: center;" class="">
 <img width="600" alt="14961079002_67719a7da2_z.jpg" src="/upload/medialibrary/429/42905854b4cd345c32105dfe27edcdfa.jpg" height="397" title="14961079002_67719a7da2_z.jpg"><br>
</p>
<p style="text-align: center;" class="">
 <span style="color: #666666; background-color: #ffffff;">Photo credit: </span><a href="https://www.flickr.com/photos/lulek/14961079002/" style="color: #fc354c;">Maciek Lulko</a><span style="color: #666666; background-color: #ffffff;"> via </span><a href="https://visualhunt.com/" style="color: #fc354c;">VisualHunt</a><span style="color: #666666; background-color: #ffffff;"> / </span><a href="https://creativecommons.org/licenses/by-nc/2.0/" style="color: #fc354c;">CC BY-NC</a><br>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>2.&nbsp;</b><b><i>Прогулка на кораблике</i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В своём исследовании города студенты обязательно должны прокатиться на кораблике, и посмотреть на Статую Свободы и Бруклинский мост с воды. «Прогулка на кораблике – лучший способ насладиться знаменитым видом небоскрёбов и сделать классические фото», - рекомендует <b>Джош Анкорс </b>из<b> L&#233;man Manhattan Preparatory School</b>. «Другой вариант – паром Стейтен Айленда: он бесплатный и даёт замечательную возможность для исследования этой части города».
</p>
<p style="text-align: center;" class="">
 <img width="600" alt="5111456589_cff5364705_z.jpg" src="/upload/medialibrary/a69/a69474d0bbb8f7a9ef459925ac4eb112.jpg" height="288" title="5111456589_cff5364705_z.jpg"><br>
</p>
<p style="text-align: center;" class="">
 <span style="color: #666666; background-color: #ffffff;">Photo credit: </span><a href="https://www.flickr.com/photos/jacobgarcia/5111456589/" style="color: #fc354c;">Jacobo Garcia</a><span style="color: #666666; background-color: #ffffff;"> via </span><a href="https://visualhunt.com/" style="color: #fc354c;">Visual hunt</a><span style="color: #666666; background-color: #ffffff;"> / </span><a href="https://creativecommons.org/licenses/by-nd/2.0/" style="color: #0abfbc;">CC BY-ND</a><br>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>3.&nbsp;</b><b><i>Парад русалок в Кони Айленд</i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Парад проводится в конце июня вне зависимости от погоды и знаменует собой начало летнего сезона и празднование карнавала Марди Гра, который проводился в начале 20 века. Участники парада каждый год надевают костюмы русалок и других морских жителей всевозможных форм и размеров. Во время парада выбирают Короля и Королеву русалок, в роли которых зачастую выступают знаменитости, как, например, Лу Рид и Квин Латифа. «Это потрясающий летний фестиваль с новой и альтернативной музыкой, который проводится в финансовом районе рядом с South Street Seaport и Brookfield Place», - восхищается <b>Дэвид Хьюз </b>из<b> ILSC New York</b>.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>4. </b><b><i>Капитолий в Олбани</i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Капитолий стоит посетить и ради истории, и ради его архитектуры: здание строилось вручную в течение 25 лет, и с 1880-х гг. здесь заседает правительство штата Нью-Йорк. «Экскурсии проводятся в Капитолии каждый день. Для посещения лучше выбрать хорошую погоду, так как помимо самого здания вы сможете осмотреть и прилегающие территории, а также подняться на башню Корнинг для панорамного вида», - советует <b>Тара Хеффернан</b> из колледжа <b>Saint Rose</b>.
</p>
<p style="text-align: center;" class="">
 <img width="600" alt="26413938812_8bb13baf27_z.jpg" src="/upload/medialibrary/b75/b75c1d9ba73b4eb96648a3042eedf1c2.jpg" height="402" title="26413938812_8bb13baf27_z.jpg"><br>
</p>
<p style="text-align: center;" class="">
 <span style="color: #666666; background-color: #ffffff;">Photo credit: </span><a href="https://www.flickr.com/photos/onasill/26413938812/" style="color: #fc354c;">Onasill ~ Bill Badzo</a><span style="color: #666666; background-color: #ffffff;"> via </span><a href="https://visualhunt.com/" style="color: #fc354c;">Visual Hunt</a><span style="color: #666666; background-color: #ffffff;"> / </span><a href="https://creativecommons.org/licenses/by-nc-sa/2.0/" style="color: #fc354c;">CC BY-NC-SA</a><br>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>5.&nbsp;</b><b><i>Прюденшал-центр в Ньюарке</i></b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Этот спортивный комплекс, с любовью прозванный «Скала», сыграл важную роль в возрождении города. Центр был открыт в 2007 году и является домашним стадионом хоккейной команды «Нью Джерси Дэвилс». Студенты часто ходят сюда на матчи. «Это также очень популярная сцена для музыкальных концертов – практически каждая звезда когда-либо выступала здесь», - уверяет <b>Джерри Докармо </b>из<b> Harvest Institute</b>.
